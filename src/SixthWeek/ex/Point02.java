package SixthWeek.ex;

public class Point02 {
    private int x, y;

    public Point02(){
        this.x = x;
        this.y = y;
    }

    public Point02(int x, int y){
        this.x = x;
        this.y = y;
    }

    public void showPoint() {
        System.out.println("(" + x + ", " + y + ")");
    }
}
